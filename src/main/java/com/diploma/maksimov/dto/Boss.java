package com.diploma.maksimov.dto;

public class Boss extends Employee{
    private Long id;
    private String info;

    public Boss() {
    }

    @Override
    public Long getId() {
        return id;
    }

    @Override
    public void setId(Long id) {
        this.id = id;
    }

    public String getInfo() {
        return info;
    }

    public void setInfo(String info) {
        this.info = info;
    }
}
